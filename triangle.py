def area(a, b, c):
    # Выводит в конcоль площадь треугольника.
    # Параметры: a, b, c (int) - стороны треугольника;
    # Возвращаемое значение: (a+b+c)/2 (float) - площадь.
    return (a + b + c) / 2


def perimeter(a, b, c):
    # Выводит в конcоль периметр треугольника.
    # Параметры: a, b, c (int) - стороны треугольника;
    # Возвращаемое значение: a+b+c (float) - периметр.
    return a + b + c
