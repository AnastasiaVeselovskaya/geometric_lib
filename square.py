
def area(a):
    # Выводит в конcоль площадь квадрата.
    # Параметры: a (int) - сторона квадрата;
    # Возвращаемое значение: a*a (int) - радиус.
    return a * a


def perimeter(a):
    # Выводит в конcоль периметр квадрата.
    # Параметры: a (int) - сторона квадрата;
    # Возвращаемое значение: 4*a (int) - периметр.
    return 4 * a
