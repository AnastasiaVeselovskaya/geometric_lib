def area(a, h):
    # Выводит в конcоль площадь треугольника.
    # Параметры: a, (int) - сторонa треугольника;
    #            h (int) - высота, проведённая к стороне a;
    # Возвращаемое значение: a* h / 2 (float) - площадь.
    return a * h / 2


def perimeter(a, b, c):
    # Выводит в конcоль периметр треугольника.
    # Параметры: a, b, c (int) - стороны треугольника;
    # Возвращаемое значение: a+b+c (float) - периметр.
    return a + b + c
